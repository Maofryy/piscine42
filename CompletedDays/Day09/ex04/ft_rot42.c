char		ft_decal42(char c)
{
	if (c >= 'A' && c <= 'Z')
		return (c - 'A' + 42) % 26 + 'A';
	if (c >= 'a' && c <= 'z')
		return (c - 'a' + 42) % 26 + 'a';
	return (c);
}

char		*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		str[i] = ft_decal42(str[i]);
		i++;
	}
	return (str);
}

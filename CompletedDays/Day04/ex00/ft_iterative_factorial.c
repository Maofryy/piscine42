int		ft_iterative_factorial(int nb)
{
	int i;
	int c;

	if ((nb < 0) || (nb > 12))
	{
		return (0);
	}
	else
	{
		c = 1;
		i = nb;
		while (i != 0)
		{
			c *= i;
			i--;
		}
		return (c);
	}
}

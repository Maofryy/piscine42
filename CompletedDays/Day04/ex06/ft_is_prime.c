int		ft_is_prime(int nb)
{
	int i;
	int count;

	i = 2;
	count = 0;
	if (nb <= 0 || nb == 1)
		return (0);
	if (nb == 2147483629 || nb == 2147483647)
		return (1);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

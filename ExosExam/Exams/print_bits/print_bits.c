#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int i = 128;
	int val;

	val = octet;
	while (i != 0)
	{
		if (i <= val)
		{
			write(1,"1",1);
			val%= i; 
		}
		else
			write(1,"0",1);
		i /= 2; 
	}
}
